package org.linagora.linshare.ldap;

public class LdapGroupMemberObject {

	protected String firstName;

	protected String lastName;

	protected String email;

	protected String externalId;

	protected Role role;

	public LdapGroupMemberObject() {
		super();
	}

	public LdapGroupMemberObject(String firstName, String lastName, String email, String externalId) {
		super();
		this.firstName = firstName;
		this.lastName = lastName;
		this.email = email;
		this.externalId = externalId;
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getExternalId() {
		return externalId;
	}

	public void setExternalId(String externalId) {
		this.externalId = externalId;
	}

	public Role getRole() {
		return role;
	}

	public void setRole(Role role) {
		this.role = role;
	}

	@Override
	public String toString() {
		return "LdapGroupMemberObject [firstName=" + firstName + ", lastName=" + lastName + ", email=" + email
				+ ", role=" + role + ", externalId=" + externalId + "]";
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((externalId == null) ? 0 : externalId.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		LdapGroupMemberObject other = (LdapGroupMemberObject) obj;
		if (externalId == null) {
			if (other.externalId != null)
				return false;
		} else if (!externalId.equals(other.externalId))
			return false;
		return true;
	}

}
