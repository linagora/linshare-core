package org.linagora.linshare.core.domain.objects;

import java.util.Calendar;

public class FileInfo {

	private String name;
	private String path;

	private Long size;
	private String mimeType;
	private String uuid;
	private Calendar lastModified;
	


	public FileInfo(){
		
	}
	
	public FileInfo(String uuid,String name,String path){
		this.uuid=uuid;
		this.name=name;
		this.path=path;

	}
	
	public FileInfo(String uuid,String name,String path,Long size){
		this.uuid=uuid;
		this.name=name;
		this.path=path;

		this.size=size;
		
	}
	
	public FileInfo(String uuid,String name,String path,Long size,String mimeType){
		this.uuid=uuid;
		this.name=name;
		this.path=path;

		this.size=size;
		this.mimeType=mimeType;
		
	}
	
	public String getMimeType() {
		return mimeType;
	}

	public void setMimeType(String mimeType) {
		this.mimeType = mimeType;
	}

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}

	public Long getSize() {
		return size;
	}
	public void setSize(Long size) {
		this.size = size;
	}

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	public Calendar getLastModified() {
		return lastModified;
	}

	public void setLastModified(Calendar lastModified) {
		this.lastModified = lastModified;
	}
	
	
	
}
